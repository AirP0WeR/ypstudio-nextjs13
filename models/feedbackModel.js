import mongoose from 'mongoose';

const feedbackModel = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const feedback = mongoose.model('Feedback', feedbackModel);

export default feedback;
